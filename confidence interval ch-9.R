# confidence interval for two sample normal means, pg 364
# 1. Define Sample Data and Population Variances
# Replace these with your actual data and known variances
sample1_data <- c(10, 12, 15, 11, 9, 14, 16, 13, 12, 10)
sample2_data <- c(20, 22, 25, 21, 19, 24, 26, 23, 22, 20)

pop_var1 <- 4.0   # Known population variance for sample 1
pop_var2 <- 5.0   # Known population variance for sample 2

# 2. Calculate necessary sample statistics
n1 <- length(sample1_data)
n2 <- length(sample2_data)
mean1 <- mean(sample1_data)
mean2 <- mean(sample2_data)
diff_means <- mean1 - mean2

# 3. Define the confidence level and find the critical Z-value
conf_level <- 0.95
alpha <- 1 - conf_level
# qnorm(1 - alpha/2) gives the Z-value for the specified confidence level (e.g., 1.96)
z_critical <- qnorm(1 - alpha/2) 

# 4. Calculate the standard error of the difference in means
std_error_diff <- sqrt(pop_var1 / n1 + pop_var2 / n2)

# 5. Calculate the Margin of Error (ME)
margin_of_error <- z_critical * std_error_diff

# 6. Calculate the Confidence Interval bounds
lower_bound <- diff_means - margin_of_error
upper_bound <- diff_means + margin_of_error

# 7. Print the results
cat("Difference in Sample Means (x1 - x2):", diff_means, "\n")
cat("standard error difference:", std_error_diff,"\n")
cat("Margin of Error:", margin_of_error, "\n")
cat(conf_level * 100, "% Confidence Interval for (mu1 - mu2): [", 
    lower_bound, ", ", upper_bound, "]\n")



