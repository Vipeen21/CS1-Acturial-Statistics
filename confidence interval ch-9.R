# no built in function for calculating confidence intervals for the variance in r
# so we use the Chi-squared distribution
# 1. Sample Data (replace with your own data)
set.seed(42)
sample_data <- rnorm(n = 30, mean = 50, sd = 10)

# 2. Define Confidence Level
conf_level <- 0.95
alpha <- 1 - conf_level

# 3. Calculate necessary sample statistics
n <- length(sample_data)
sample_var <- var(sample_data)
degrees_freedom <- n - 1

# 4. Find the critical Chi-squared values
# The interval for variance is NOT symmetrical, so we need two different critical values:
# Lower Chi-squared value (for the upper bound of CI formula)
chi_sq_upper <- qchisq(p = alpha / 2, df = degrees_freedom, lower.tail = FALSE)

# Upper Chi-squared value (for the lower bound of CI formula)
chi_sq_lower <- qchisq(p = 1 - (alpha / 2), df = degrees_freedom, lower.tail = FALSE)
# Alternatively: chi_sq_lower <- qchisq(p = alpha / 2, df = degrees_freedom, lower.tail = TRUE)


# 5. Calculate the Confidence Interval for the population variance (sigma^2)
# Formula: [ ((n - 1) * s^2) / B , ((n - 1) * s^2) / A ]
# Where A and B are the respective chi-squared values
lower_bound_var <- (degrees_freedom * sample_var) / chi_sq_upper
upper_bound_var <- (degrees_freedom * sample_var) / chi_sq_lower

# 6. Print the results
cat("Sample Variance (s^2):", sample_var, "\n")
cat(conf_level * 100, "% Confidence Interval for Variance [σ^2]: [", 
    lower_bound_var, ", ", upper_bound_var, "]\n")

# If you also need the Confidence Interval for the Standard Deviation (σ):
lower_bound_sd <- sqrt(lower_bound_var)
upper_bound_sd <- sqrt(upper_bound_var)
cat(conf_level * 100, "% Confidence Interval for Std Dev [σ]: [", 
    lower_bound_sd, ", ", upper_bound_sd, "]\n")



