# difference between two poisson parameters
# Define counts of events and their corresponding time/exposure units
counts1 <- 150
exposure1 <- 500  # e.g., 10 days, 10 person-years
counts2 <- 650
exposure2 <- 4500  # e.g., 15 days, 15 person-years

# Calculate sample rates
rate1_hat <- counts1 / exposure1
rate2_hat <- counts2 / exposure2

# Calculate the difference in rates
diff_rates <- rate1_hat - rate2_hat

# Define the confidence level and Z-value (1.96 for 95% CI)
conf_level <- 0.95
z_critical <- qnorm(1 - (1 - conf_level) / 2) 
print(z_critical)
# Calculate the Standard Error of the difference
# Since mean = variance in Poisson, the variance estimate is the rate itself
SE_diff <- sqrt(rate1_hat / exposure1 + rate2_hat / exposure2)

# Calculate Margin of Error
margin_of_error <- z_critical * SE_diff

# Calculate the Confidence Interval bounds
lower_bound <- diff_rates - margin_of_error
upper_bound <- diff_rates + margin_of_error

cat("Difference in Rates (lambda1 - lambda2):", diff_rates, "\n")
cat("Margin of Error:", margin_of_error, "\n")
cat(conf_level * 100, "% Confidence Interval for Difference: [", 
    lower_bound, ", ", upper_bound, "]\n")





