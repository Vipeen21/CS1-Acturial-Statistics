# f test and its relevancy for t test for equality or inequality of variances, page 368
# Sample Data
sample1 <- c(10.2, 12.5, 11.8, 9.7, 13.1, 10.5, 12.0)
sample2 <- c(15.1, 18.8, 12.2, 11.5, 19.9, 17.9)

# 1. Perform the F-test for equality of variances
variance_test_result <- var.test(sample1, sample2)

print(variance_test_result)
p_value_f_test <- variance_test_result$p.value

# 2. Use an if-else condition to choose the appropriate t-test
if (p_value_f_test >= 0.05) {
  cat("P-value for F-test (", round(p_value_f_test, 3), ") is large. Using POOLED t-test.\n")
  t_test_result <- t.test(sample1, sample2, var.equal = TRUE)
} else {
  cat("P-value for F-test (", round(p_value_f_test, 3), ") is small. Using WELCH'S t-test.\n")
  # Welch's is R's default, so var.equal=FALSE is implicit
  t_test_result <- t.test(sample1, sample2) 
}

# 3. Print the results of the chosen t-test
print(t_test_result)




